#!/usr/bin/python3

def add(a, b):
    """Addition function

    Args:
        a: 1st input.
        b: 2nd input.

    Returns:
        Returns a + b
    """
    return (a + b)


def sub(a, b):
    """Subtraction func

    Args:
        a: 1st param
        b: 2nd param

    Returns:
        a - b
    """
    return (a - b)


def mul(a, b):
    """multiplication func

    Args:
        a: 1st arg.
        b: 2nd arg.

    Returns:
        a * b
    """
    return (a * b)


def div(a, b):
    """division func

    Args:
        a: 1st arg.
        b: 2nd arg.

    Returns:
        a / b
    """
    return (a / b)
